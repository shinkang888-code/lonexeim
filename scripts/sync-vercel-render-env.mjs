#!/usr/bin/env node
/**
 * Pull Render lonex* service URLs → Vercel NEXT_PUBLIC_* env + optional deploy.
 * Usage: node scripts/sync-vercel-render-env.mjs [--deploy]
 */
import { execSync, spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const HUB = path.join(ROOT, "lonex-hub");
const RENDER_ENV = path.join(ROOT, "scripts", "docker-oss.render.env");
const PROJECT = "lonexeim-hub";
const RENDER =
  process.env.RENDER_CLI ||
  path.join(process.env.LOCALAPPDATA || "", "render-cli", "render.exe");

/** Render service name → Vercel env key */
const MAP = {
  "lonex-oss-livekit": {
    NEXT_PUBLIC_LIVEKIT_URL: (url) => url.replace(/\/$/, ""),
    NEXT_PUBLIC_LIVEKIT_MEET_URL: (url) => {
      const base = url.replace(/\/$/, "");
      const wss = base.replace(/^https:/, "wss:");
      return `https://meet.livekit.io/custom?liveKitUrl=${encodeURIComponent(wss)}`;
    },
  },
};

function run(cmd) {
  execSync(cmd, { cwd: HUB, stdio: "inherit", shell: true });
}

function renderServices() {
  const out = execSync(`"${RENDER}" services -o json`, {
    encoding: "utf8",
    stdio: ["pipe", "pipe", "pipe"],
  });
  return JSON.parse(out).map((x) => x.service);
}

function setVercelEnv(key, value, target) {
  const trimmed = value.trim();
  const escaped = trimmed.replace(/"/g, '\\"');
  run(`vercel env add ${key} ${target} --force --yes --value "${escaped}"`);
  console.log(`  + ${key} (${target})`);
}

const deploy = process.argv.includes("--deploy");
const lonex = renderServices().filter((s) => s.name?.startsWith("lonex"));
const envLines = [
  "# Auto-generated by sync-vercel-render-env.mjs",
  `# Created: ${new Date().toISOString()}`,
  "",
];
const vercelUpdates = new Map();

for (const svc of lonex) {
  const url = svc.serviceDetails?.url;
  if (!url) continue;
  const mapping = MAP[svc.name];
  if (!mapping) continue;
  for (const [envKey, fn] of Object.entries(mapping)) {
    const val = fn(url);
    envLines.push(`${envKey}=${val}`);
    vercelUpdates.set(envKey, val);
  }
  console.log(`${svc.name} → ${url}`);
}

if (vercelUpdates.size === 0) {
  console.log("No mapped Render URLs (lonex-oss-livekit web URL required).");
  console.log("Create via: .\\scripts\\render-deploy.ps1 -Action create");
  process.exit(0);
}

fs.writeFileSync(RENDER_ENV, envLines.join("\n") + "\n");
console.log(`Wrote ${RENDER_ENV}`);

run(`vercel link --project ${PROJECT} --yes`);
for (const [key, value] of vercelUpdates) {
  for (const target of ["production", "preview", "development"]) {
    setVercelEnv(key, value, target);
  }
}

if (deploy) {
  run("vercel deploy --prod --yes");
  console.log("\nHub: https://lonexeim-hub.vercel.app");
}
